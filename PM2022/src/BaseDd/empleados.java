package BaseDd;


public class empleados {
    private String nombre_emp;
    private String apellido_emp;
    private String cedula_emp;
    private String telefono_emp;
    private String cargo_emp;
    private int sueldo_emp;
    private String contrasena_emp;

    public empleados() {
    }

    public String getNombre_emp() {
        return nombre_emp;
    }

    public void setNombre_emp(String nombre_emp) {
        this.nombre_emp = nombre_emp;
    }

    public String getApellido_emp() {
        return apellido_emp;
    }

    public void setApellido_emp(String apellido_emp) {
        this.apellido_emp = apellido_emp;
    }

    public String getCedula_emp() {
        return cedula_emp;
    }

    public void setCedula_emp(String cedula_emp) {
        this.cedula_emp = cedula_emp;
    }

    public String getTelefono_emp() {
        return telefono_emp;
    }

    public void setTelefono_emp(String telefono_emp) {
        this.telefono_emp = telefono_emp;
    }

    public String getCargo_emp() {
        return cargo_emp;
    }

    public void setCargo_emp(String cargo_emp) {
        this.cargo_emp = cargo_emp;
    }

    public int getSueldo_emp() {
        return sueldo_emp;
    }

    public void setSueldo_emp(int sueldo_emp) {
        this.sueldo_emp = sueldo_emp;
    }

    public String getContrasena_emp() {
        return contrasena_emp;
    }

    public void setContrasena_emp(String contrasena_emp) {
        this.contrasena_emp = contrasena_emp;
    }

  
    
    

}
